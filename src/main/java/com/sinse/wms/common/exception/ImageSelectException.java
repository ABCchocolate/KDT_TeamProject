package com.sinse.wms.common.exception;

public class ImageSelectException extends RuntimeException{

	public ImageSelectException() {
		super();
	}

	public ImageSelectException(String message, Throwable cause) {
		super(message, cause);
	}

	public ImageSelectException(String message) {
		super(message);
	}

	public ImageSelectException(Throwable cause) {
		super(cause);
	}
}
