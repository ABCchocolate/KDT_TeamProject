package com.sinse.wms.common.exception;

public class ImageInsertException extends RuntimeException{

	public ImageInsertException() {
		super();
	}

	public ImageInsertException(String message, Throwable cause) {
		super(message, cause);
	}

	public ImageInsertException(String message) {
		super(message);
	}

	public ImageInsertException(Throwable cause) {
		super(cause);
	}
}
