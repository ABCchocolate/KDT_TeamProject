public class ProgressStatus {
	private int progressStatus_id;
	private String progressStatus_name;
	
	public int getProgressStatus_id() {
		return progressStatus_id;
	}
	public void setProgressStatus_id(int progressStatus_id) {
		this.progressStatus_id = progressStatus_id;
	}
	public String getProgressStatus_name() {
		return progressStatus_name;
	}
	public void setProgressStatus_name(String progressStatus_name) {
		this.progressStatus_name = progressStatus_name;
	}
}
